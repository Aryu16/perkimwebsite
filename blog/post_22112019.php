				<p>Medan – “Pembangunan kawasan terintegritas Mebidangro ini merupakan salah satu upaya untuk mewujudkan visi Presiden Jokowi. “Ada lima instruksi Presiden, prioritas pembangunan SDM sejak dalam kandungan, permudah investasi untuk membuka lapangan kerja, reformasi birokrasi, APBN harus tepat sasaran, hingga pembangunan infrastruktur. Jadi, Mebidangro merupakan salah satu upaya mewujudkan visi Presiden</p>

				<p>Hal itu disampaikan Gubernur Sumatera Utara (Sumut) Edy Rahmayadi saat membuka acara Focus Group Discussion (FGD) Rencana Program Pengembangan Jaringan Jalan Mebidangro dan pambahasan pembangunan KEK Sei mangkei, di Hotel Aryaduta, Jalan Kapten Maulana Lubis Nomor 8, Kota Medan, Rabu (20/11).</p>

				<p>Dalam sambutanya, Gubernur juga menjelaskan, pembangunan kawasan terintegritas Mebidangro ini merupakan salah satu upaya untuk mewujudkan visi Presiden Jokowi. “Ada lima instruksi Presiden, prioritas pembangunan SDM sejak dalam kandungan, permudah investasi untuk membuka lapangan kerja, reformasi birokrasi, APBN harus tepat sasaran, hingga pembangunan infrastruktur. Jadi, Mebidangro merupakan salah satu upaya mewujudkan visi Presiden tersebut,” ujar Edy Rahmayadi.</p>

				<p>Hedy Rehadian, Direktur Jalan Bebas Hambatan dan Perkotaan Kementerian Pekerjaan Umum dan Perumahan Rakyat (PUPR) yang hadir dalam kegiatan tersebut menyampaikan, Kementerian PUPR mendukung upaya percepatan pembangunan kawasan Mebidangro. Karena itu pihaknya hadir untuk mensinkronkan seluruh kebutuhan konektivitas, antara setiap kabupaten yang memiliki kebutuhan.</p>

				<p>Kemudian acara dilanjukan pembahasan mengenai Pembangunan Kawasan KeK Sei Mangkei</p>


				<div class="image">
					<img src="../blog/images/sinegritassumutbermartabat2.jpg" alt="" />
				</div><br>

				<p>Kawasan  Ekonomi  Khusus  (KEK)  Sei  Mangkei ditetapkan  melalui  Peraturan  Pemerintah Nomor  29  Tahun  2012  pada  tanggal  27 Februari 2012 dan merupakan KEK pertama di Indonesia  yang  telah diresmikan  beroperasi oleh  Presiden  Joko  Widodo  pada  27  Januari 2015.  KEK Sei Mangkei yang berlokasi di Provinsi Sumatera Utara memiliki bisnis utama berupa industri kelapa sawit dan karet dan difokuskan untuk menjadi pusat pengembangan industri kelapa sawit dan karet hilir berskala besar dan berkualitas internasional.</p>

				<p>Sebagai kawasan industri yang berada di sentra bahan baku berbasis agro dan dekat dengan Selat Malaka, KEK Sei Mangkei juga memiliki bisnis pendukung yaitu logistik dan pariwisata. Dengan total luas lahan sebesar 2.002,7 ha, KEK Sei Mangkei terbuka akan potensi industri lainnya terutama di sektor hilir dengan nilai tambah yang tinggi. KEK Sei Mangkei didukung dengan infrastruktur di dalam dan luar kawasan</p>

				<p>Salah satu dukungan yang dibutuhkan dalam pengembangan KEK Sei Mangkei adalah pembangunan hunian bagi para pekerja.  Hunian yang dapat disediakan pemerintah adalah Rusunawa yang dilengkapi dengan Prasarana sarana dan utilitas Umum permukiman yang terintegrasi dengan fasilitas dasar ekonomi, pendidikan dan kesehatan. Ketersediaan fasilitas tersebut akan membuat kawasan permukiman tumbuh berkembang dan mandiri.</p>


				<div class="image">
					<img src="../blog/images/sinegritassumutbermartabat3.jpg" alt="" />
				</div><br>

				<p>Dalam Kesempatan Kadis PKP Sumut Ida Mariana menyampaikan, “Pembangunan Rusunawa di KEK Sei Mangkei sudah sampai tahap menuju  Kerjasama Pemerintah dengan Badan Usaha (KPBU) tetapi di dalam kawasan terintegrasi ini perlunya dukungan utamanya air minum dukungan pembangunan pasar, pembangunan sekolah kemudian sarana olah raga dan rumah sakit semua terintegrasi karena nantinya kita akan membackup pekerja yang ada di Sei Mangkei”.</p>

				<p>Beliau juga mengatakan “Selain pembangunan Rusunawa Untuk MBR lokasi ini nantinya akan dibangun apartement yang diperuntukan bagi Top maneger”.</p>

				<p>Acara tersebut merupakan Senergisitas yang sangat baik, kita ketahui infrastruktur arus jalur darat merupakan jalur utama masyarakat Sumut dalam beraktifitas terutama di Kawasan Ekonomi Khusus Sei Mangkei dimana Dinas PKP Sumut nanti nya akan dibangun Rusunawa diperuntukan bagi pekerja-perkerja di Sei mangkei.</p>


