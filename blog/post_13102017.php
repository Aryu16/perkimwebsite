							<div class="lower-content">

			<p>Dalam rangka mendukung penyelenggaraan Kawasan Siap Bangun (Kasiba) dan Lingkungan Siap Bangun (Lisiba) di Provinsi Sumatera Utara khususnya terkait kebijakan penyediaan Kasiba dan Lisiba Lintas Kabupaten/Kota, serta untuk mensinergikan dan menyelaraskan langkah antar Pemerintah Provinsi, Pemerintah Kabupaten/Kota dan unsur terkait bidang perumahan, Dinas Perumahan dan Kawasan Permukiman Provinsi Sumatera Utara menyelenggarakan Acara “Sosialisasi Perancangan Kebijakan Penyediaan Kasiba dan Lisiba Lintas Kabupaten/Kota di Sumatera Utara TA. 2017”, pada tanggal 5 s.d. 6 Oktober 2017, bertempat di Hotel Garuda Plaza – Medan.</p>

			<p>Acara dibuka pada pukul 14.00 WIB, Hari Kamis, tanggal 5 Oktober 2017, oleh Bapak Sekretaris Daerah Provinsi Sumatera Utara, Bapak Ir. Ibnu S. Hutomo, MM. Dalam sambutan pembukaan beliau menyampaikan bahwa sebagai upaya mendukung pembangunan Perumahan dan Kawasan Permukiman (PKP) di Provinsi Sumatera Utara, khususnya perkembangan ekonomi lokal dan perkembangan kota, serta dalam upaya pemenuhan perumahan dan permukiman jangka pendek, jangka menengah dan jangka panjang, dimana salah satu kendala dan permasalahan yang dihadapi terkait menurunnya kualitas lingkungan permukiman adalah minimnya PSU (prasarana, sarana dan utilitas umum) dan keterbatasan lahan untuk kawasan permukiman, maka sangat diperlukan adanya upaya untuk penyelenggaraan Kasiba dan Lisiba di kabupaten/kota. Untuk itu diharapkan dari acara sosialisasi ini dapat menghasilkan kesepakatan terkait kebijakan penyediaan Kasiba dan Lisiba Kabupaten/Kota di Provinsi Sumatera Utara yang tepat sasaran, efektif, optimal dan berkelanjutan.</p>

			<div class="image">
				<img src="../blog/images/penyediaankasibadanlisiba2.jpg" alt="" />
			</div><br>

				<p>Acara Sosialisasi Perancangan Kebijakan Penyediaan Kasiba dan Lisiba Lintas Kabupaten/Kota di Sumatera Utara TA. 2017 ini menghadirkan 1 orang narasumber dari Kementerian PUPR dan 3 orang narasumber dari Pemerintah Provinsi Sumatera Utara. Narasumber dari Kementerian PUPR adalah Bapak Direktur Perencanaan Direktorat Penyediaan Perumahan Kemen PUPR, Bapak Ir. Hardi Simamora, MT. Beliau menyampaikan materi terkait Arah Kebijakan Penyediaan Kasiba dan Lisiba Lintas Kabupaten/Kota di Provinsi Sumatera Utara. Narasumber dari Pemerintah Provinsi Sumatera Utara antara lain Ibu Ir. Ida Mariana, M.Si (Kepala Dinas Perumahan dan Kawasan Permukiman Provsu), dengan materi terkait Tata Cara Penyelenggaraan dan Penetapan Kasiba Lisiba Pada Lintas Kabupaten/Kota di Provinsi Sumatera Utara, Bapak Ir. Panusunan Harahap, M.Si. (Kasubbid Kawasan Stategis dan Kerjasama, Bidang Sarana, Prasarana dan Kewilayahan, Bappeda Provsu), dengan materi terkait Peluang Pelaksanaan Kasiba dan Lisiba Lintas Kabupaten/Kota Yang Telah Di Perda-kan, dan Bapak Ir. Syafrizal Pane, SH. (Kasie Penatagunaan Tanah, Kanwil BPN Provsu) dengan materi tentang Gambaran Status Lahan Pada Arah Pengembangan Permukiman Menurut Perda RTRW Kabupaten/Kota di Provinsi Sumatera Utara.</p>

			<div class="image">
				<img src="../blog/images/penyediaankasibadanlisiba3.jpg" alt="" />
			</div><br>

			<p>Adapun peserta yang diundang dan hadir dalam acara Sosialisasi Perancangan Kebijakan Penyediaan Kasiba dan Lisiba Lintas Kabupaten/Kota di Sumatera Utara TA. 2017 ini terdiri dari SKPD terkait tingkat provinsi (Bappeda Provsu, BPN Provsu, BPS Provsu, Dinas SDA – CKTR Provsu, Dinas Penanaman Modal dan Pelayanan Terpadu Satu Pintu Provsu, Satuan Kerja SNVT Penyediaan Perumahan Provsu, Satuan Kerja Pengembangan Kawasan Permukiman Provsu), SKPD terkait tingkat Kabupaten/Kota (Bappeda dan Dinas PKP dari 21 Kabupaten/Kota di Provinsi Sumatera Utara yang sudah Perda RTRW), serta unsur-unsur terkait PKP di Provsu (Perum Perumnas Wilayah I, DPD REI Sumut, APERSI Sumut serta pihak bank BTN).</p>

			<div class="image">
				<img src="../blog/images/penyediaankasibadanlisiba4.jpg" alt="" />
			</div><br>

			<p>Sesuai dengan UU Nomor 1 Tahun 2011, yang dimaksud dengan definisi lahan skala besar / Kasiba adalah lokasi yang mampu menampung sekurang-kurangnya 3.000 unit rumah, baik lokasi yang belum terbangun maupun integrasi lokasi pembangunan baru dan lokasi yang sudah ada permukimannya. Sedangkan untuk Lisiba adalah lokasi yang mampu menampung sekurang-kurangnya 1.000 unit rumah, baik lokasi yang belum terbangun maupun integrasi lokasi pembangunan baru dan lokasi yang sudah ada permukimannya. Narasumber juga menyampaikan bahwa Kasiba dan Lisiba mempengaruhi penyediaan Prasarana, Sarana dan Utilitas Umum (PSU) yang memenuhi pembakuan pelayanan sesuai Rencana Tata Ruang Wilayah (RTRW), sekaligus menjadi sarana penyediaan kavling tanah matang, beserta rumah dengan pola hunian yang berimbang, terencana dan terjangkau bagi seluruh lapisan masyarakat, sehingga pada akhirnya Kasiba dan Lisiba sangat mempengaruhi dan merupakan salah satu komponen pengendali harga tanah.</p>

			<div class="image">
				<img src="../blog/images/penyediaankasibadanlisiba5.jpg" alt="" />
			</div><br>

			<p>Dipimpin oleh Bapak Ir. Suherman (Sekretaris Dinas PKP Provsu) selaku Ketua Panitia acara Sosialisasi ini, pada hari kedua (Jum’at, 6 oktober 2017), disusun bersama-sama Nota Kesepakatan Antara Pemerintah Provinsi Sumatera Utara Dengan Pemerintah Kabupaten/Kota Tentang Penyediaan Kasiba dan Lisiba di Kabupaten/Kota, ditandatangani oleh peserta acara/perwakilan dari dinas PKP masing-masing Kabupaten/Kota dan Ibu Ir. Ida Mariana, M.Si. selaku Kepala Dinas PKP Provsu. Nota Kesepakatan tersebut merupakan bentuk kesepahaman dan kesepakatan terkait penyediaan Kasiba Lisiba yang nantinya akan ditindaklanjuti dan disampaikan ke masing-masing Kabupaten/Kota. Selanjutnya acara ditutup oleh Bapak Ir. Suherman pada pukul 12.00 Hari Jum’at tanggal 6 Oktober 2017.</p>


