				<p>Medan – Salah satu faktor keberhasilan dari Program Sejuta Rumah adalah Peraturan Perundangan. Peraturan perundangan merupakan dasar Yuridis bagi pemerintah dan seluruh aktor pembangunan dalam melakukan setiap langkah menuju pencapaian target pembangunan.</p>

				<p>Hal itu disampaikan Sekretaris Daerah Provinsi Sumatera Utara yang diwakili oleh Asisten Perekonomian, Pembangunan dan Kesejahteraan Setdaprovsu, Nouval Mahyar saat memberikan sambutan dan pembukaan acara Rapat Penyusunan Peraturan Gubernur Tentang Hunian Berimbang dan CSR Bidang Perumahan dan Kawasan Permukiman di Hotel Grandhika, 19-20 Desember 2019.</p>

				<p>“Peraturan perundangan  juga menyatakan visi, strategi, kewenangan hingga aturan teknis bagi setiap pemangku kepentingan dalam melaksanakan program dan kegiatan pembangunan bidang perumahan dan kawasan permukiman”, lanjutnya.</p>

				<p>“namun bila melihat implemantasi program perumahan selama ini, justru peraturan perundangan yang menjadi salah satu titik lemah khususnya di tingkat Pemerintah Provinsi dan Kabupaten/kota, hal ini dikarenakan kelangkaan peraturan daerah dalam peraturan Gubernur yang seharusnya mengarahkan dan mengatur berbagai aspek dalam urusan perumahan dan kawasan permukiman”, Ucapnya.</p>


