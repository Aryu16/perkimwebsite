				<p>“Tanda bantuan dari pengembangan yaitu di luar dari pemerintah, Pemerintah tidak akan sanggup untuk membenahi permasalahan rumah tidak layak huni di Sumatera Utara ini, maka dari itu kami dari pemerintah meminta partisipainya, mari Kita bersama membangun Sumatera Utara menjadi lebih baik lagi”, ucap Ida.</p>

