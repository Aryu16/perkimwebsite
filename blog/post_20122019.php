				<p>Usai sambutan dari Sekretaris daerah Provinsi Sumatera Utara acara kemudian di lanjutkan, Pengarahan yang di sampaikan oleh Kepala Dinas Perumahan dan Kawasan Pemukiman Ida Mariana.</p>


