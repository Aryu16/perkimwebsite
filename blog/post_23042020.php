				<p>Medan – Kepala Dinas Perumahan dan Perumahan Ida Mariana Harahap mengikuti Musrenbang RKPD Provinsi Sumatera Utara Tahun 2021. Musrembang dilakukan secara online via aplikasi ZOOM terkait dengan pembatasan pertemuan yang melibatkan banyak orang dalam masa pandemic COVID-19. Gubernur Sumatera Utara Edy Rahmayadi melakukan pembukaan Musrembang. Ketua DPRD Provinsi, Perwakilan Menteri Dalam Negeri Tumpak Haposan Simanjuntak, dan Menteri Perencanaan Nasional Suharso Monoarfa turut memberikan kata sambutan dan Arahan. Musrembang Provsu diikuti oleh seluruh Kepala Daerah Kab/Kota, Kepala OPD Pemprovsumut.</p> 

				<p>Musrenbang RKPD Provsu 2021 merupakan tindaklanjut dari pelaksanaan pra musrenbang yang dibagi dalam 4 wilayah zona, Wilayah Pantai Barat dilakukan di Kab. Mandailing Natal pada 21 Februari 2020, Wilayah Pantai Timur di Kab. Langkat pada 25-26 Februari 2020, Wilayah Dataran Tinggi di Kab. Karo pada 6 maret 2020 dan terakhir Wilayah Kepulauan Nias yang direncanakan di Kab. Nias Selatan pada 18-19 Maret 2020 tetapi dibatalkan karna terhalang pandemic COVID-19, sampai Maret 2020 sudah 22 Kabupaten Kota yang melaksanakan musrenbang.</p>

				<p>Musrenbang RKPD Provinsi Sumatera  Tahun 2020 dimulai tanggal 23 April yang berisikan arahan langsung dari Gubernur Sumatera Utara dan arahan dari Menteri Dalam Negeri yang diwakilkan oleh Tumpak Haposan Simanjuntak dan Menteri Perencanaan Nasional Suharso Manoarfa.</p>

				<p>Musrenbang ini merupakan amanat dari UU No.25 Tahun 2004  Tentang Sistem Perencanaan Nasional, PP No. 44 Tahun 2008 Tentang Tahapan Tata Cara Penyusunan dan Pengendalian dan Evaluasi Perencanaan Pembangunan Daerah, Kemendagri Tahun 2011 tentang Tata Cara Perencanaan Evaluasi Rancangan Perkuat Daerah tentang LPJPD dan LPJMP serta tata cara pembangunan LPJPD, LPJMP dan LKPD.</p>

				<div class="card" style="width:100%;">
 				 <img src="../blog/images/musrenbangrkpdprovsumut2.jpg" class="card-img-top" alt="...">
  				<div class="card-body">
    			<p class="card-text"><strong>Gubernur Sumatera Utara “Edy Rahmayadi” dan Wakil Gubernur Sumatera Utara “Musa Rajekshah”</strong></p>
