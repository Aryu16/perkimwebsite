			<p>Tumpak Haposan Simanjuntak menjelaskan 5 Arahan Utama Presiden Jokowi, yaitu Pembangunan SDM, Pembangunan Infrastuktur Penyerdehanaan Regulasi, Penyerdehanaan Birokrasi dan Transformasi Ekonomi. RKP 2021 sebelum COVID-19 meningkatkan Industri, Pariwisata dan Investasi di Berbagai Wilayah Didukung oleh SDM, dan Infrastuktur untuk Pertumbuhan berkualitas, sementara RKPD Sumut Tahun 2021 adalah Pembangunan SDM Unggul, Peningkatan Infrastuktur dan Pengembangan Wilayah dalam Percepatan Pemulihan Ketahanan Ekonomi dan Kehidupan Masyarakat. Perubahan RKP 2021 setelah terjadi COVID-19 “Mempercepat Pemulihan Ketahanan Ekonomi dan Kehidupan Masyarakat, dengan Fokus pada Industri, Pariwisata, Investasi, dan Penguatan Sistem Kesehatan Nasional” ujarnya.</p>

			<p>Menteri Perencanaan Nasional Suharso Manoarfa menyampaikan arahan Presiden dalam rangka membendung COVID-19 ; Meningkatkan pengujian sampel secara massif dan melaksanakan isolasi secara ketat terhadap orang dalam pemantauan serta pasien dalam pengawasan. Memanfaatkan teknologi informasi berupa aplikasi kesehatan atau telemedicine ,sehingga orang tidak perlu bertemu dengan dokter atau kerumah sakit. Melakukan komunikasi secara efektif dan transparan kepada public dengan detil dan baik. Menegakkan hukum dengan bantuan aparat negara, sehingga masyarakat memiliki kedisiplinan yang kuat dalam rangka memutus rantai penyebaran Covid-19. Menjaga kelancaran distribusi logistic yang dibutuhkan masyarakat dalam kebijakan tanggap darurat, baik kelancaran logistic dari pusat ke daerah maupun dari gudang-gudang logistik ke daerah.</p>

