			<p>Suharso Manoarfa juga menyampaikan Pemulihan dan Transfomasi Ekonomi Daerah Refocusing APBD Tahun 2020 untuk mendukung penanganan Covid19, penguatan  jaring pengaman sosial,dan pemulihan ekonomi. Penguatan safeguarding (monitoring, pengendalian dan evaluasi) terhadap pelaksanaan kebijakan penanganan Covid19 termasuk penanganan kesehatan, penguatan jaring pengaman sosial dan pemulihan ekonomi  agar tetap berjalan transparan, akuntabel efisien dan efektif. Penentuan usulan kegiatan 2021 yang benar-benar mendukung prioritas nasional dalam RKP 2021, mempunyai daya ungkit bagi pemulihan perekonomian daerah, dan mendorong transformasi ekonomi. Penguatansinergi kegiatan yang dibiayai dari APBN, APBD, Dana Transfer Daerah, Dana Desa, investasi swasta dan sumber pendanaan lainnya.</p>

			<p>Penyusunan RKPD 2021 didalam situasi yang tidak pasti, karena status Siaga Darurat Bencana Non-alam Corona Virus. Pemerintah memprediksi ini akan berlangsung sampai akhir 2020. Penyusunan target kinerja kedepan perlu diperhitungkan kembali mengingat dampak luas dari bencana ini.</p>

			<p>Musrenbang Provsu dilanjutkan besok tanggal 24 April 2020 dengan arahan dari Bappeda Pemprovsu bersama OPD dan Bappeda Kab/Kota. (ulfa)</p>
			
