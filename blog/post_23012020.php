				<p><strong>Medan</strong> – Dalam melaksanakan pembangunan untuk pelayanan kebutuhan masyarakat, Dinas Perumahan dan Kawasan Permukiman berpedoman kepada pembagian urusan pemerintahan. Urusan pemerintahan yang diemban adalah urusan wajib pelayanan dasar Perumahan Rakyat Dan Kawasan Permukiman. Urusan Pemerintahan Wajib adalah Urusan Pemerintahan yang wajib diselenggarakan oleh semua Daerah yang berhak diperoleh setiap warga negara secara minimal sesuai jenis dan mutu pelayanan dasar.</p>

				<p>Berdasarkan Undang-Undang No 1 tahun 2011 tentang Perumahan Dan Kawasan Permukiman, Rumah Dan Permukiman yang layak merupakan kebutuhan dasar manusia, dan mempunyai peran yang sangat strategis dalam pembentukan watak serta kepribadian bangsa sebagai salah satu upaya membangun manusia indonesia seutuhnya, berjati diri, mandiri, dan produktif.</p>

				<p>Negara bertanggung jawab melindungi segenap bangsa indonesia melalui penyelenggaraan perumahan dan kawasan permukiman agar masyarakat mampu bertempat tinggal serta menghuni rumah yang layak dan terjangkau di dalam perumahan yang sehat, aman, harmonis, dan berkelanjutan.</p>

				<p>Hal tersebut seiring dengan misi pembangunan Provinsi Sumatera Utara yaitu mewujudkan masyarakat sumatera utara yang bermartabat dalam kehidupan karena memiliki iman dan taqwa, tersedianya sandang pangan yang cukup, <storng>rumah yang layak</storng>, pendidikan yang baik, kesehatan yang prima, mata pencaharian yang menyenangkan, serta harga-harga yang terjangkau, yang arah kebijakan pembangunan kedepannya difokuskan pada peningkatan pelayanan masyarakat melalui rehabilitasi rumah tidak layak huni, penataan dan peningkatan kualitas kawasan permukiman kumuh dengan luas 10 – 15 ha, penyelenggaraan PSU permukiman, penyediaan dan rehabilitasi rumah korban bencana Provinsi serta memfasilitasi penyediaan rumah bagi masyarakat yang terkena relokasi program Pemerintah Daerah Provinsi.</p>

				<p>Menindaklanjuti misi pembangunan tersebut, untuk program rehabilitasi rumah tidak layak huni pada tahun 2019 telah direhabilitasi rumah sejumlah 14.204 unit rumah yang dominan dilakukan oleh sumber dana APBN sejumlah 10.975 unit melalui kegiatan bantuan stimulan rumah swadaya (BSPS) dan pembangunan baru rumah berbasis komunitas total sebanyak 277 unit yang berada di Kabupaten Batu Bara untuk komunitas petugas kebersihan, Kabupaten Labuhan Batu untuk komunitas guru honor, Kabupaten Labuhan batu selatan untuk komunitas buruh sawit dan Kota Padang Sidimpuan komunitas pedagang kecil dan tukang bangunan. Melalui sumber dana APBD sebanyak 622 unit dilakukan rehabilitasi rumah bagi masyarakat kurang mampu dan sebagai penghargaan pada desa binaan PKK dan desa keluarga berencana terbaik di Provinsi Sumatera Utara serta melalui CSR badan usaha sebanyak 27 unit.</p>

				<p>Dalam hal pengurangan luas kawasan kumuh, Dinas PKP baru mampu menangani sekitar 88 ha dari target 135 ha yang ada saat ini.</p>

				<p>Dalam mendukung penyediaan rumah murah bagi masyarakat berpenghasilan rendah (MBR), Pemerintah Provinsi Sumatera Utara melalui Dinas PKP telah memberikan bantuan PSU perumahan pada 3.491 unit rumah MBR.</p>

				<p>Sedangkan untuk program penyediaan dan rehabilitasi rumah korban bencana, telah dibangun 26 unit rumah untuk korban bencana banjir dan longsor tahun 2018 di Kecamatan Ulu Pungkut Kabupaten Mandailing Natal. (dedi)</p>

