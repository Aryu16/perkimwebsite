				<p>Medan – Menindaklanjuti pengisian SKP Online sesuai dengan Peraturan Pemerintah No.46 Tahun 2011 tentang Penilaian Prestasi Kerja PNS, Dinas PKP Provsu melakukan trainning lanjutan untuk pengisian penggunaan aplikasi SKP online dalam menyusun Sasaran Kerja Pegawai yang sudah dilaksanakan Selasa 28/01 lalu.</p>

				<p>Trainning lanjutan diadakan pada, Rabu 05/02 untuk para ASN agar dapat mengatasi masalah yang dihadapi mengenai aplikasi. Acara di awali dengan pembukaan dan pengarahan oleh Ida Mariana Harahap selaku Kepala Dinas yang pada kesempatan tersebut menyampaikan aplikasi masih baru yang terkadang error atau lambat dalam membuka untuk melakukan pengisian SKP harian. Untuk lebih memahami aplikasi dan menu-menu yang dipergunakan penjelasan yang sudah dilakukan di pertemuan sebelumnya sekarang lebih kepada pemantapan dan membahas kendala yang dihadapkan dalam pengisian SKP online, sambutnya.</p>

				<div class="image">
					<img src="../blog/images/memantapkanpengisianskponline2.jpg" alt="" />
				</div><br>

				<p>Pemantapan mengenai pengisian SKP Online dibawakan oleh Hendra Siahaan sebagai perwakilan dari BKD provsu, “sebelum masuk ke aplikasi diharapkan user telah mempunyai bahan untuk input data, bahan merupakan kesepakatan antara staff dan atasan nya dan disesuaikan dengan tupoksi, analisis beban kerja sistem kerja. Sehingga staff sudah mengetahui uraian tugas, apa uraian nya berapa volume nya, berapa lama dikerjakan kapan dikerjakan, harus jelas terlebih dahulu target tahunan sebelum menginput ke aplikasi” tuturnya.</p>

				<p>Pihak yang terlibat dalam proses penyusunan sarana kinerja pegawai adalah masing-masing pegawai, atasan langsung, atasanan Pembanding serta Badan Kepegawaian Daerah (BKD). Usulan target kinerja yang akan diverifikasi atasan langsung akan diteruskan ke BKD untuk diverifikasi. Kesepakatan usulan target kinerja inilah yang akan dilaporkan setiap bulan realistisnya. Aktivitas harian masing-masing pegawai juga dilaporkan melalui aplikasi SKP online.</p>

				<p>Acara dilanjutkan dengan tanya jawab sampai berakhirnya kegiatan dan dihadiri oleh ASN Struktural dan staf di lingkungan Dinas PKP Provsu. Kemudian acara ditutup oleh Herizal Pulungan selaku Pl Sekertaris Dinas.(ulfa)</p>


