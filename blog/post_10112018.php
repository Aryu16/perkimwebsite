			<p>“ Selama banteng-banteng Indonesia masih mempunyai darah merah yang dapat membikin secarik kain putih merah dan putih maka selama itu tidak akan kita mau menyerah kepada siapapun juga”</p>

			<p>(Pidato Bung Tomo di radio pada saat pertempuran menghadapi Inggris di Surabaya bulan November 1945)</p>

			<p>Pesan Ir. Soekarno</p>

			<p>“Berikan aku 1000 orang tua, niscaya akan kucabut semeru dari akarnya dan berikan aku 10 pemuda, niscaya akan ku guncangkan Dunia”</p>

