							<div class="lower-content">
								

			<p>Setelah terbunuhnya Brigadir Jenderal Mallaby, penggantinya, Mayor Jenderal Robert Mansergh mengeluarkan ultimatum yang menyebutkan bahwa semua pimpinan dan orang Indonesia yang bersenjata harus melapor dan meletakkan senjatanya di tempat yang ditentukan dan menyerahkan diri dengan mengangkat tangan di atas. Batas ultimatum adalah jam 6.00 pagi tanggal 10 November 1945.</p> 

			<p>Ultimatum tersebut kemudian dianggap sebagai penghinaan bagi para pejuang dan rakyat yang telah membentuk banyak badan-badan perjuangan / milisi. Ultimatum tersebut ditolak oleh pihak Indonesia dengan alasan bahwa Republik Indonesia waktu itu sudah berdiri, dan TKR (Tentara Keamanan Rakyat) juga telah dibentuk sebagai pasukan negara. Selain itu, banyak organisasi perjuangan bersenjata yang telah dibentuk masyarakat, termasuk di kalangan pemuda, mahasiswa dan pelajar yang menentang masuknya kembali pemerintahan Belanda yang memboncengi kehadiran tentara Inggris di Indonesia.</p>

			<p>Pada 10 November pagi, tentara Inggris mulai melancarkan serangan. Pasukan sekutu mendapatkan perlawanan dari pasukan dan milisi Indonesia.</p>

			<div class="image">
				<img src="../blog/images/upacaraharipahlawan2.jpg" alt="" />
			</div><br>

			<p>Selain Bung Tomo terdapat pula tokoh-tokoh berpengaruh lain dalam menggerakkan rakyat Surabaya pada masa itu, beberapa datang dari latar belakang agama seperti KH. Hasyim Asy’ari, KH. Wahab Hasbullah serta kyai-kyai pesantren lainnya juga mengerahkan santri-santri mereka dan masyarakat sipil sebagai milisi perlawanan (pada waktu itu masyarakat tidak begitu patuh kepada pemerintahan tetapi mereka lebih patuh dan taat kepada para kyai/ulama) sehingga perlawanan pihak Indonesia berlangsung alot, dari hari ke hari, hingga dari minggu ke minggu lainnya. Perlawanan rakyat yang pada awalnya dilakukan secara spontan dan tidak terkoordinasi, makin hari makin teratur. Pertempuran ini mencapai waktu sekitar tiga minggu.</p>

			<p>Setidaknya 6,000 – 16,000 pejuang dari pihak Indonesia tewas dan 200,000 rakyat sipil mengungsi dari Surabaya. Korban dari pasukan Inggris dan India kira-kira sejumlah 600 – 2000 tentara.Pertempuran berdarah di Surabaya yang memakan ribuan korban jiwa tersebut telah menggerakkan perlawanan rakyat di seluruh Indonesia untuk melakukan perlawanan. Banyaknya pejuang yang gugur dan rakyat sipil yang menjadi korban pada hari 10 November ini kemudian dikenang sebagai Hari Pahlawan oleh Republik Indonesia hingga sekarang.</p>

			<p>Untuk menghargai jasa para Pahlawan-pahlawan Negara Kesatuan Republik Indonesia,Dinas Perumahan dan Kawasan Permukiman Provinsi Sumatera Utara melaksanakan Upacara dalam rangka memperingati hari pahlawan yang jatuh pada tanggal 10 November 2018, dalam Pelaksanaan Upacara Tersebut Bapak Ir. Syahbudi Siregar, MM di Tunjuk Sebagai Inspektur Upacara, Tema Hari Pahlawan Tahun 2018 adalah <strong>“Semangat Pahlawan Di Dadaku”</strong> Mengandung makna sesuai fitrahnya dalam diri setiap insan tertanam nilai – nilai Kepahlawanan, oleh karenanya siapapun dapat menjadi pahlawan, setiap warga Negara Indonesia tanpa kecuali dapat berinisiatif mengabdikan hal yang bermanfaat untuk kemashlahatan diri, lingkungan sekitar, bagi bangsa dan Negara.</p>

			<p>Negeri ini membutuhkan pemuda yang kokoh dengan jati dirinya, mempunyai karakter local yang luhur, percaya diri dan peka terhadap permasalahan social sehingga mampu terlibat dalam usaha-usaha kesejahteraan social, memberikan pelayanan social bagi mereka yang membutuhkan pertolongan social.</p>

			<p>Negeri ini juga membutuhkan pemuda yang mempunyai pandangan global, mampu berkolaborasi untuk kemajuan bangsa dan mampu memanfaatkan kemajuan teknologi untuk menjadikan Indonesia diperhitungkan dalam bersaing dan bersanding dengan Negara lain khususnya ketika memasuki <strong>era revolusi industry 4.0.</strong></p>

			<p>Upacara yang di hadiri oleh seluruh penjabat dan staff lingkungan Dinas Perumahan dan Kawasan Permukiman Provinsi Sumatera Utara tersebut berlangsung sangat hikmat, para perserta upacara hingga terbawa suasana saat lagu hening Cipta di Kumandangkan, suasana-suasana kesediahan, keperihan yang di rasakan para pahlawan saat terluka, saat gugur bahkan saat berhasil mempertahan kan NKRI sangat dirasakan para peserta Upacara.</p>


