							<div class="lower-content">

			<p>Dari masa ke masa kebutuhan air minum semakin meningkat. Hal ini disebabkan karena pertumbuhan penduduk yang semakin pesat, dan tingkat konsumsi yang meningkat. Secara nasional pada tahun 2013 baru <strong>67,72% masyarakat Indonesia yang terlayani</strong> dengan akses air minum yang aman. Penyelenggara Air Minum PDAM masih dihadapkan pada beberapa masalah diantaranya masalah pengembangan usaha dan perluasan pelayanan, masalah penyesuaian tarif, masalah teknis seperti kebocoran yang relatif masih tinggi, masalah keuangan, kelembagaan atau kapasitas sumber daya manusia, serta kepemimpinan dan profesionalisme.</p>

			<p>Dan saat ini fakta menunjukkan bahwa perlunya integrasi dana APBN dan APBD maupun kemampuan PDAM dan Swasta untuk mengejar kebutuhan pelayanan air minum yang terus meningkat terutama dalam memenuhi target cakupan pelayanan akses aman nasional sebesar 100% di tahun 2019.  Kerjasama pemerintah dengan dunia usaha (KPS) belum mampu mencukupi kekurangan (gap) yang ada dalam perluasan akses pelayanan air minum.</p>

			<p>Pemerintah telah memberikan bantuan kepada daerah untuk meningkatkan kemampuan PDAM dan peningkatan akses pelayanan air minum, namun demikian bantuan tersebut belum secara signifikan berdampak pada peningkatan sambungan rumah atau peningkatan akses pelayanan. Bantuan diantaranya mencakup bantuan teknis dalam rangka menyehatkan PDAM, bantuan manajemen sebagai pembinaan SDM, peningkatan efisiensi, serta bantuan program sebagai optimalisasi fungsi SPAM yang ada, perbaikan komponen, untuk menurunkan biaya operasional dan mengembalikan fungsi Sistem.</p>

			<p>Pengembangan SPAM memerlukan dukungan aspek pendanaan, aspek kelembagaan, aspek teknis, aspek peran serta masyarakat / swasta dan yang juga penting yaitu aspek PENGATURAN.</p>

			<p>Aspek pengaturan yang ada di tingkat Pusat saat ini sudah cukup lengkap, dimulai dari UndangUndang nomor 7 tahun 2004 tentang Sumber Daya Air, lalu Peraturan Pemerintah nomor 16 tentang Pengembangan SPAM serta Peraturan-Peraturan Menteri terkait pengembangan SPAM lainnya yang merupakan amanat langsung dari Perundangan dimaksud. Aspek pengaturan yang lengkap di tingkat pusat belum disesuaikan / ditindaklanjuti dengan aspek pengaturan di tingkat daerah. Peraturan-Peraturan terkait pengembangan SPAM di daerah masih terbatas, pada Peraturan Daerah tentang Pembentukan PDAM dan Peraturan Daerah tentang Pelayanan PDAM.</p>

			<div class="image">
				<img src="../blog/images/pengembanganspam2.jpg" alt="" />
			</div><br>


			<p><strong>Pengaturan</strong> yang lengkap, jelas, dan terpadu, akan sangat menentukan keberhasilan pengembangan SPAM karena merupakan landasan kebijakan upaya dimaksud. Salah satu upaya dalam aspek pengaturan untuk meningkatkan cakupan pelayanan air minum adalah dengan melengkapi peraturan terkait pengembangan SPAM di daerah. Menindaklanjuti hal tersebut, sesuai dengan salah satu fungsi BPPSPAM dalam PP 16 tahun 2005 tentang Pengembangan Sistem Penyediaan Air Minum, pasal 46 ayat b, yaitu membantu Pemerintah dan Pemerintah Daerah dalam penerapan norma, standar, pedoman, dan manual (NSPM) oleh penyelenggara dan masyarakat. Pada tahun 2014 BPPSPAM melakukan Kegiatan Pendampingan Penyusunan Kebijakan dan Strategi Daerah (Jakstrada) Pengembangan SPAM untuk Provinsi/ Kabupaten / Kota.</p>

			<p>Peraturan Pemerintah Nomor 16 Tahun 2005 tentang Pengembangan Sistem Penyediaan Air Minum mengamanatkan penyusunan Kebijakan dan Strategi Nasional Pengembangan Sistem Penyediaan Air Minum serta Kebijakan dan Strategi Daerah Pengembangan Sistem Penyediaan Air Minum. Amanat tersebut selanjutnya ditindaklanjuti oleh Kementerian Pekerjaan Umum dengan menetapkan Peraturan Menteri Pekerjaan Umum Nomor 20/PRT/M/2006 dan saat ini telah diperbaharui menjadi Peraturan Menteri Pekerjaan Umum Nomor 13/PRT/M/2013 tentang Kebijakan dan Strategi Nasional Pengembangan Sistem Penyediaan Air Minum. Dalam Peraturan Menteri tersebut dijelaskan bahwa kebijakan dan strategi pengembangan system penyediaan air minum merupakan arah pengembangan system penyediaan air minum dalam 5 (lima) tahun mendatang, dan sebagai pedoman bagi Pemerintah Daerah dan penyelenggara pengembangan system penyediaan air minum dalam melaksanakan penyelenggaraan pengembangan system penyediaan air minum.</p>

			<p>Sejalan dengan hal tersebut, dalam PP 16 tahun 2005 juga mengamanatkan kepada Kabupaten/Kota untuk menyusun Rencana Induk Sistem Penyediaan Air Minum (RISPAM) agar daerah memiliki perencanaan pengembangan Air Minum untuk jangka waktu yang panjang, namun dalam pelaksanaannya sampai dengan saat ini baru 83 Pemerintah Kabupaten/Kota yang telah menyelesaikan RISPAM. Dokumen RISPAM dan dokumen JAKSTRADA Pengembangan SPAM merupakan dasar dalam penyusunan RPI2JM Kabupaten/Kota.</p>

			<p>Mengingat pentingnya penyusunan RISPAM dan JAKSTRADA untuk pengembangan SPAM di tingkat Pemerintah Daerah sesuai dengan amanat Peraturan Perundangan dimaksud (PP 16/2005 dan PP 38/2007) serta agar pemerintah daerah lebih terarah dalam melaksanakan pengembangan system penyediaan air minum di daerah, maka BPPSPAM Kementerian Pekerjaan Umum mempunyai tugas dalam membantu pemerintah daerah untuk menyusun Jakstrada pengembangan SPAM serta akan memfasilitasi daerah dalam bentuk pendampingan kepada Provinsi/ Kabupaten/ Kota untuk menyusun dokumen dimaksud diatas. Untuk pendampingan awal yang dilakukan tahun 2014 ini ditujukan kepada Kabupaten/Kota yang telah menyusun RISPAM.</p>

			<p>Dengan adanya aspek pengaturan yang jelas, pengembangan SPAM di daerah baik perpipaan dan non perpipaan (BJP) yang dilakukan oleh para penyelenggara (PDAM maupun non PDAM dapat dipantau kinerjanya serta keberlanjutannya sehingga dapat mewujudkan pelayanan kepada masyakarat yang terjamin K3 nya (Kualitas, Kuantitas dan Kontinuitas).</p>

			
