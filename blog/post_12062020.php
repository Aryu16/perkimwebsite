				<p>Pembangunan Rusunawa Terintegrasi Bagi Pekerja KEK Sei Mangkei dan hinterland diprediksi akan segera bangkit dan bertumbuh seiring dengan terwujudnya pembangunan Kota Baru dengan berbagai fasilitas social maupun komersil. Selanjutnya  Ida Mariana juga menjelaskan setelah Market Sounding hari ini (12/06/2020)  akan dilanjutkan dengan Periode Penyampaian Pertanyaan oleh Investor, Periode Pemberian Jawaban atas pertanyaan oleh PJPK, dan Penyampaian feedback questionaire and Letter of interest melalui email. Lebih kurang 10 hari kedepan akan dilakukan Video Conference yang ke-2 bagi investor yang mengajukan LOI.</p>

				<div class="image">
					<img src="../blog/images/gugustugaspenanganancovid3.jpg" alt="" />
				</div>

				<p>Sehingga pada video conference selanjutnya sudah diketahui investor mana yang akan berminat sehingga bisa melanjutkan ke proses berikutnya yaitu pengajuan PDF ke Kementerian Keuangan, kemudian penyusunan FBC, setelah FBC akan dilanjutkan bidding kemudian kontrak, dilanjutkan  kontruksi dan diharapkan selesai ditahun 2020, dan Pembangunan dapat dimulai pada triwulan I Tahun 2021. Memang Time line  yang disajikan sangat optimis dapat dilakukan, walaupun pada dasarnya dibutuhkan waktu yang cukup untuk menelaah teknis dan non teknis yang harus dilakukan. PJPK berharap untuk segera merealisasikan Pembangunannya di tahun 2021. PJPK tetap dengan semangat dan berkomitmen tinggi untuk tetap melaksanakan tugas dan tanggung jawabnya demikian juga dengan Kementerian lainnya (APBN) yang turut memberikan dukungan pembangunan seperti Kementerian PUPR, Kementerian Kesehatan dan Kementerian Perdagangan juga ikut mendukung apa yang sudah di programkan pada Pembanunan Rusunawa Terintegrasi di KEK Sei Mangkei.</p>

