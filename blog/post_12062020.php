				<p>Medan – Gubernur Sumatera Utara Edy Rahmayadi bersama Sekretaris Daerah R. Sabrina dan Kepala Dinas Perumahan dan Kawasan Permukiman Provinsi  Sumatera Utara Ida Mariana melaksanakan acara  Market Sounding  Pembangunan Rusunawa Terintegrasi Bagi Pekerja KEK Sei Mangkei melalui Video Conference bersama dengan Tim KPBU Provinsi Sumatera Utara, BUMD, Kementerian PUPR, Kementerian PPN/Bappenas, Kementerian Keuangan, BPKPM, PT. PII, PT. Sarana Multi Finance, dan calon investor Dalam dan Luar Negeri.</p> 

				<p>Market Sounding dibuka oleh Gubernur Sumatera Utara Edy Rahmayadi dalam sambutannya beliau mengapresiasi kinerja Kadis Perkim Provsu dan seluruh jajarannya.</p>

