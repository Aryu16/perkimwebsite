				<p>Ida Mariana juga menjelaskan latar belakang masalah perlunya Pembangunan Rusunawa Terintegrasi bagi pekerja KEK Sei Mangkei karena tingginya angka backlog perumahan di Sumatera Utara serta menjawab Kebutuhan Rumah bagi pekerja/karyawan KEK Sei Mangkei jyang sangat mendesak.</p>

				<p>MBR Hiterland SIBASA dan Tebing Tinggi perlu menjadi perhatian termasuk yang akan menyumbang kebutuhan Rumah di KEK Sei Mangkei karena berbatasan langsung, hal ini memberikan efek negative serta memberi pengaruhi  yang signifikan terhadao lambannya pemngembangan wilayah yang sejalan dengan lambannya pertumbuhan ekonomi.</p>

				<div class="image">
					<img src="../blog/images/gugustugaspenanganancovid2.jpg" alt="" />
