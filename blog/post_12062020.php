				<p>Pembangunan Rusunawa Terintegrasi bagi pekerja KEK Sei Mangkei melalui KPBU Provsu akan dilakukan secara professional dan proposional. Perlu diketahui bersama bahwa Pembangunan Rusunawa Terintegrasi KEK Sei Mangkei merupakan Pilot Proyek bagi Provinsi Sumatera Utara dilengkapi dengan fasilitas yang memadai. Sasaran Pembangunan Rusunawa Terintegrasi adalah tenaga kerja KEK Sei Mangkei, MBR Sibasa dan Hiterland serta Ex Patriat KEK Sei Mangkei.</p>

				<div class="image">
