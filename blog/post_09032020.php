				<p>Medan – Dinas Perumahan dan Kawasan Permukiman Provsu mendapatkan kunjungan Sosialisasi  mengenai sistem informasi surat masuk dan keluar (SISUMAKER) dari Dinas Komunikasi dan Informatika (Kominfo) Provsu. Kedepannya, surat menyurat sudah akan dilakukan dengan teknologi elektronik untuk meningkatkan efektifitas dan efisiensi dalam pelaksanaan sistem persuratan.</p>

				<p>Rido Meivala sebagai perwakilan Kominfo yang menyampaikan Sosialisasi mengenai SISUMAKER. Hadirin yang hadir dalam sosialisasi tersebut adalah seluruh eselon IV dan eselon III di Dinas Perumahan dan Kawasan Permukiman provsu. Para Eselon 4 dan Eselon 3 terlebih dahulu harus membuat akun di <a href="http://sisumaker.sumutprov.go.id/">http://sisumaker.sumutprov.go.id</a> untuk bisa menggunakan SISUMAKER.</p>

				<p>“Di dalam sistem dijelaskan bahwa draft surat keluar dibuat oleh eselon IV naik ke eselon III kemudian ke Sekretaris dan akhirnya naik ke Kepala OPD. Jika di dalam draft surat ada kesalahan atau kurang sesuai, penerima surat bisa melakukan penolakan draft surat dan draft surat akan kembali ke pengirimnya, penerima draft surat bisa juga langsung melakukan revisi dan melanjutkan surat hingga ke Kepala OPD” jelasnya.</p>

