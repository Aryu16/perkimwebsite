				<p>Penerapan Sistem Pemerintahan Berbasis Elektronik (SPBE) sebagaimana diamanatkan dalam Peraturan Gubernur Sumatera Utara Nomor 19 Tahun 2019 Tentang Pengembangan e-government Provinsi Sumatera Utara Tahun 2019-2023, sesuai dengan Reformasi birokrasi pada hakikatnya merupakan upaya untuk melakukan pembaharuan dan perubahan mendasar terhadap sistem penyelenggaraan pemerintahan terutama menyangkut aspek-aspek kelembagaan (organisasi), ketatalaksanaan (business prosess) dan sumber daya manusia aparatur. Maka dari itu sesuai dengan Peraturan Presiden Republik Indonesia Nomor 95 Tahun 2018 Tentang Sistem Pemerintahan Berbasis Elektronik. Sistem Pemerintahan Berbasis Elektronik yang selanjutnya disingkat SPBE adalah penyelenggaraan pemerintahan yang memanfaatkan teknologi informasi dan komunikasi untuk memberikan layanan kepada Pengguna SPBE.</p>

				<p>Pada saat ini terjadi perubahan era yang dikenal dengan era revolusi industri 4.0 yang ditandai dengan mekanisasi produksi untuk menunjang efektivitas dan efesiensi aktivitas manusia. Era revolusi industri 4.0 tidak saja memiliki dampak terhadap dunia usaha dan industri namun juga terhadap lingkungan birosistekrasi pemerintah. Harapan dengan sistem ini terlaksana arsip surat lebih tertata, mendukung paperless, dapat melaksanakan tugas dengan lebih cepat dengan pemanfaatan teknologi.(ulfa)</p>


