			<p><strong>Medan</strong> –   Pemerintah Pusat dalam hal ini Kementerian Pekerjaan Umum Dan Perumahan Rakyat melalui Ditjen Pembiayaan Perumahan melakukan Rapat Koordinasi dalam rangka pemenuhan Rumah Bagi Masyarakat Berpenghasilan Rendah (MBR) oleh Bank Pembangunan Daerah (BPD) Se- Sumatera melalui Dukungan Electronic Mortgage Management System (e-MMS), Jumat (2/8) di Hotel Karibia Jalan Timor Medan.</p> 

			<p>Rapat yang dibuka Langsung Oleh Gubernur Sumatera Utara Edy Rahmayadi dihadiri Unsur Kementerian Pekerjaan Umum dan Perumahan Rakyat, Otoritas Jasa Keuangan, Bank Pembangunan Daerah se- Sumatera, Dinas Perumahan dan Kawasan Permukiman se- Sumatera, Asisten Perekonomian dan Pembangunan se-Sumatera, Asosiasi Pengembang Perumahan serta PT. Sarana Multigriya Finansial (PT.SMI).</p>

			<p>Dalam sambutannya Gubernur Edy Rahmayadi menyampaikan Backlog rumah di Sumut mencapai 411.000 unit Tahun 2015 dan untuk Indonesia sebanyak 7,4 juta unit. Beliau mengharapkan setelah gong pembukaan dipukul semua bisa berjalan agar masyarakat bisa memiliki rumah yang layak dan nyaman.</p>

			<p>Dirjen Pembiayaan Insfrastruktur Pekerjaan Umum dan Perumahan Kementerian PUPR RI Eko Heripoerwanto mengatakan, realisasi penyaluran bantuan pembiayaan perumahan FLPP dan subsidi selisih bunga (SSB) tahun 2015 – 2018 mencapai 775.508 unit rumah dengan anggaran sebesar Rp21,06 triliun. Untuk tahun 2019, total anggaran yang telah disiapkan untuk FLPP, SSB, Subsidi Bantuan Uang Muka (SBUM), dan Bantuan Pembiayaan Perumahan Berbasis Tabungan (BP2BT) sebesar Rp11,5 triliun.</p>
			
			<div class="image">
					<img src="../blog/images/EMMS2.jpg" alt="" />
				</div><br>

			<p>Hingga 30 Juli 2019, kata Eko, realisasi penyaluran bantuan pembiayaan perumahan telah mencapai sebesar Rp5,04 triliun untuk 48.463 unit rumah. “Realisasi FLPP oleh BPD se-Sumatera per Juli 2019 sebanyak 15.580 unit dengan nilai FLPP sebesar Rp1,42 triliun. Khusus BPD Sumatera Utara sebesar 3.691 unit dengan nilai FLPP sebesar Rp332,4 miliar,” katanya. Hingga saat ini Kementerian PUPR melalui Direktorat Jenderal Pembiayaan Infrastruktur Pekerjaan Umum dan Perumahan (DJPI) telah melakukan berbagai upaya untuk meningkatkan pembiayaan perumahan di daerah. Namun, realisasi penyaluran bantuan pembiayaan perumahan di daerah masih kurang optimal.</p>

			<p>Kurang optimalnya realisasi pembiayaan perumahan di daerah, katanya, dapat disebabkan oleh beberapa faktor diantaranya lemahnya koordinasi pelaku pembiayaan perumahan, tidak ada data perumahan yang valid dan terintegrasi yang didukung dengan sistem teknologi informasi, dukungan APBD bidang perumahan yang masih terbatas, kurangnya keterlibatan dan dukungan dari Pemerintah Daerah sebagai pemegang saham BPD (shareholders), maupun sebagai pemangku kepentingan utama (stakeholders) di bidang perumahan dan kurangnya partisipasi BPD dalam penyaluran bantuan pembiayaan perumahan.</p>

			<p>Memperhatikan kondisi dan permasalahan ini, kata Eko, Kementrian PUPR meyakini bahwa BPD sebagai bank pelaksana KPR Bersubsidi perlu didorong dan melakukan transformasi agar dapat bersaing dan memberikan kontribusi yang signifikan bagi pertumbuhan ekonomi daerah yang berkelanjutan. “BPD dirasa sangat sesuai menjadi penyalur KPR Bersubsidi karena memiliki infrastruktur yang kuat hingga ke pelosok daerah dan memahami karakteristik masyarakat pada masing-masing daerah. Selain itu, sebagai perbankan lokal, BPD dapat lebih efisien dalam memfasilitasi masyarakat yang membutuhkan bantuan perumahan,” jelasnya.</p>

			<div class="image">
					<img src="../blog/images/EMMS3.jpg" alt="" />
				</div><br>


			<p>Perhatian pemerintah pusat ke Sumatera Utara sangat diharapkan terutama dalam waktu dekat ini diharapkan adanya penambahan Quota pembiyaan sehingga minimal 15.000 unit, agar rumah yang ready stok dapat dimiliki MBR di Sumut dengan mendapat manfaat dari FLPP. Hal ini secara khusus disampaikan oleh Kepala Dinas Perumahan dan Kawasan Permukiman Provinsi Sumatera Utara Ida Mariana Harahap. Pengenalan e-MMS oleh PT. SMI adalah salah satu bentuk dukungan dalam penyiapan SDM yang terampil dalam memanfaatkan kemajuan teknologi informasi.</p>


