							<div class="lower-content">

			<p>Memperingati hari kesatuan Pancasia 1 Oktober, Dinas Perumahan dan Kawasan Permukiman Provinsi Sumatera Utara melakukan upacara bendera di halaman Kantor Dinas PKP Provsu  Jl. A.H. Nasution, Medan Johor, Minggu (1/10/2017) pagi. Upacara yang dihadiri seluruh pegawai Dinas Perumahan dan Kawasan Permukiman Provinsi Sumatera Utara sekitar pukul 08:00, dipimpin Sekretaris Dinas Bapak Ir. Suherman.</p>

			<div class="image">
				<img src="../blog/images/peringatanharikelahiranpancasila2.jpg" alt="" />
			</div><br>

				<p>Hari Kesaktian Pancasila berkaitan dengan peristiwa G30S/PKI yang terjadi 30 September 1965. Pada tanggal 30 September 1965, terjadi tragedi yang dinamakan Gerakan 30 September (G30S), ketika tujuh perwira tinggi militer Indonesia beserta beberapa orang lainnya dibunuh dalam suatu usaha kudeta. Peristiwa tersebut diyakini dilakukan oleh PKI yang ingin mengganti ideologi bangsa dengan komunisme. Penghianatan PKI yang terjadi tersebut berhasil di tumpas, sehingga Negara Republik Indonesia yang berideologi Pancasila dapat berdiri sampai dengan saat ini. Pemerintah kemudian menetapkan 30 September sebagai Hari Peringatan Gerakan 30 September G30S dan tanggal 1 Oktober ditetapkan sebagai Hari Kesaktian Pancasila.</p>

			<div class="image">
				<img src="../blog/images/peringatanharikelahiranpancasila3.jpg" alt="" />
			</div><br>

			<p>Pada upacara Peringatan Hari Kesaktian Pancasila dibacakan ikrar, yaitu :</p>

			<p><strong>IKRAR</strong></p>

			<p>Dengan Rahmat Tuhan Yang Maha Esa, kami yang melakukan upacara ini menyadari sepenuhnya:</p>

			<p>bahwa sejak diproklamasikan Kemerdekaan Negara Kesatuan Republik Indonesia pada tanggal 17 Agustus 1945 pada kenyataannya telah banyak terjadi rongrongan baik dari dalam negeri maupun luar negeri terhadap Negara Kesatuan Republik Indonesia;</p>

			<p>bahwa rongrongan tersebut dimungkinkan oleh karena kelengahan, kekurangwaspadaan Bangsa Indonesia terhadap kegiatan yang berupaya menumbangkan Pancasila sebagai ideologi Negara;</p>

			<p>bahwa dengan semangat kebersamaan yang dilandasi oleh nilai-nilai luhur ideologi Pancasila, bangsa Indonesia tetap dapat memperkokoh tegaknya Negara Kesatuan Republik Indonesia;</p>

			<p>maka di hadapan Tuhan Yang Maha Esa dalam memperingati Hari Kesaktian Pancasila, kami membulatkan tekad untuk tetap mempertahankan dan mengamalkan nila-nilai Pancasila sebagai sumber kekuatan menggalang kebersamaan untuk memperjuangkan, menegakkan kebenaran dan keadilan demi keutuhan Negara Kesatuan Republik Indonesia.</p>

			<p>Butir – butir pengamalan Pancasila ditetapkan berdasarkan ketetapan MPR No. I/MPR/2003,</p>

			<p><strong>Sila pertama</strong></p>

			<p>1.	Bangsa Indonesia menyatakan kepercayaannya dan ketakwaannya terhadap Tuhan Yang Maha Esa.</p>

			<p>2.	Manusia Indonesia percaya dan takwa terhadap Tuhan Yang Maha Esa, sesuai dengan agama dan kepercayaannya masing-masing menurut dasar kemanusiaan 		 yang adil dan beradab.</p>

			<p>3.	Mengembangkan sikap hormat menghormati dan bekerja sama antara pemeluk agama dengan penganut kepercayaan yang berbeda-beda terhadap Tuhan Yang 			Maha Esa.</p>

			<p>4.	Membina kerukunan hidup di antara sesama umat beragama dan kepercayaan terhadap Tuhan Yang Maha Esa.</p>

			<p>5.	Agama dan kepercayaan terhadap Tuhan Yang Maha Esa adalah masalah yang menyangkut hubungan pribadi manusia dengan Tuhan Yang Maha Esa.</p>

			<p>6.	Mengembangkan sikap saling menghormati kebebasan menjalankan ibadah sesuai dengan agama dan kepercayaannya masing-masing.</p>

			<p>7.	Tidak memaksakan suatu agama dan kepercayaan terhadap Tuhan Yang Maha Esa kepada orang lain.</p>

			<p><strong>Sila kedua</strong></p>

			<p>1.	Mengakui dan memperlakukan manusia sesuai dengan harkat dan martabatnya sebagai makhluk Tuhan Yang Maha Esa.</p>
			<p>2.	Mengakui persamaan derajat, persamaan hak, dan kewajiban asasi setiap manusia, tanpa membeda-bedakan suku, keturunan, agama, kepercayaan, jenis 		kelamin, kedudukan sosial, warna kulit dan sebagainya.</p>
			<p>3.	Mengembangkan sikap saling mencintai sesama manusia.</p>
			<p>4.	Mengembangkan sikap saling tenggang rasa dan tepa selira.</p>
			<p>5.	Mengembangkan sikap tidak semena-mena terhadap orang lain.</p>
			<p>6.	Menjunjung tinggi nilai-nilai kemanusiaan.</p>
			<p>7.	Gemar melakukan kegiatan kemanusiaan.</p>
			<p>8.	Berani membela kebenaran dan keadilan.</p>
			<p>9.	Bangsa Indonesia merasa dirinya sebagai bagian dari seluruh umat manusia.</p>
			<p>10.	Mengembangkan sikap hormat menghormati dan bekerja sama dengan bangsa lain.</p>

			<p><strong>Sila ketiga</strong></p>

			<p>1.	Mampu menempatkan persatuan, kesatuan, serta kepentingan dan keselamatan bangsa dan negara sebagai kepentingan bersama di atas kepentingan pribadi 		   dan golongan.</p>
			<p>2.	Sanggup dan rela berkorban untuk kepentingan negara dan bangsa apabila diperlukan.</p>
			<p>3.	Mengembangkan rasa cinta kepada tanah air dan bangsa.
			<p>4.	Mengembangkan rasa kebanggaan berkebangsaan dan bertanah air Indonesia.
			<p>5.	Memelihara ketertiban dunia yang berdasarkan kemerdekaan, perdamaian abadi, dan keadilan sosial.
			<p>6.	Mengembangkan persatuan Indonesia atas dasar Bhinneka Tunggal Ika.
			<p>7.	Memajukan pergaulan demi persatuan dan kesatuan bangsa.</p>

			<p><strong>Sila keempat</strong></p>

			<p>1.	Sebagai warga negara dan warga masyarakat, setiap manusia Indonesia mempunyai kedudukan, hak, dan kewajiban yang sama.</p>
			<p>2.	Tidak boleh memaksakan kehendak kepada orang lain.</p>
			<p>3.	Mengutamakan musyawarah dalam mengambil keputusan untuk kepentingan bersama.</p>
			<p>4.	Musyawarah untuk mencapai mufakat diliputi oleh semangat kekeluargaan.</p>
			<p>5.	Menghormati dan menjunjung tinggi setiap keputusan yang dicapai sebagai hasil musyawarah.</p>
			<p>6.	Dengan iktikad baik dan rasa tanggung jawab menerima dan melaksanakan hasil keputusan musyawarah.</p>
			<p>7.	Di dalam musyawarah diutamakan kepentingan bersama di atas kepentingan pribadi dan golongan.</p>
			<p>8.	Musyawarah dilakukan dengan akal sehat dan sesuai dengan hati nurani yang luhur.</p>
			<p>9.	Keputusan yang diambil harus dapat dipertanggungjawabkan secara moral kepada Tuhan Yang Maha Esa, menjunjung tinggi harkat dan martabat manusia, 		 nilai-nilai kebenaran dan keadilan mengutamakan persatuan dan kesatuan demi kepentingan bersama.</p>
			<p>10.	Memberikan kepercayaan kepada wakil-wakil yang dipercayai untuk melaksanakan pemusyawaratan.</p>

			<p><strong>Sila kelima</strong></p>

			<p>1.	Mengembangkan perbuatan yang luhur, yang mencerminkan sikap dan suasana kekeluargaan dan kegotongroyongan.</p>
			<p>2.	Mengembangkan sikap adil terhadap sesama.</p>
			<p>3.	Menjaga keseimbangan antara hak dan kewajiban.</p>
			<p>4.	Menghormati hak orang lain.</p>
			<p>5.	Suka memberi pertolongan kepada orang lain agar dapat berdiri sendiri.</p>
			<p>6.	Tidak menggunakan hak milik untuk usaha-usaha yang bersifat pemerasan terhadap orang lain.</p>
			<p>7.	Tidak menggunakan hak milik untuk hal-hal yang bersifat pemborosan dan gaya hidup mewah.</p>
			<p>8.	Tidak menggunakan hak milik untuk bertentangan dengan atau merugikan kepentingan umum.</p>
			<p>9.	Suka bekerja keras.</p>
			<p>10.	Suka menghargai hasil karya orang lain yang bermanfaat bagi kemajuan dan kesejahteraan bersama.</p>
			<p>11.	Suka melakukan kegiatan dalam rangka mewujudkan kemajuan yang merata dan berkeadilan sosial.</p>

			<p>Nilai-nilai yang terkadung dalam Pancasila dapat menjadikan kehidupan kita semakin lebih baik. Jadi kita harus mengamalkannya dalam kehidupan sehari-hari. Pengamalan nilai-nilai Pancasila dapat dilakukan di lingkungan keluarga, masyarakat dan tempat kerja. (Ant)</p>


