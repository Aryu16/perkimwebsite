				<p>Medan – Dinas Perumahan dan Kawasan Permukiman Provsu menerima kunjungan PANSUS DPRD Kota Gunung Sitoli bersama dengan Dinas Perkim Kota Gunung Sotoli. Kunjungan diterima di Aula Dinas PKP Provsu Jl. A.H Nasution No.20 Medan (Kamis, 27/05).</p> 

				<p>Kunjungan kerja disambut oleh Sekretaris Dinas PKP Anda Subrata didampingi oleh Pejabat Eselon III dan Eselon IV. Dalam kunjungannya PANSUS DPRD Kota Gunung Sitoli ingin berkoordinasi dan konsultasi mengenai Raperda tentang penyerahan Prasarana, Sarana dan Utilitas Perumahan. Trimen V. Harefa “Nias daerah sedang berkembang, banyak perumahan yang dibangun atas nama pribadi, tetapi tidak membangun PSU secara layak lalu setelah terbangun mereka menjual, apakah pemerintah kota bisa ikut mengintervensi kegiatan pemgembangan secara pribadi bukan perusahaan, teknis pelaksanaan PSU dari pengembang ke pemerintah seperti apa, agar kami yang melakukan pengawasan bisa secara efektif ?” ujarnya.</p>

				<p>Arosokhi Harefa “Di Gunung Sitoli saat ini banyak perumahan yang pengembangnya sudah bangkrut dan belum menyerahkan PSU ke pemda, bagaimana pemda bisa ikut membantu menangani PSU yang belum diserahkan ke pemda? tambahnya.</p>

				<div class="image">
				<img src="../blog/images/menerimakunjunganpansusdprdgunungsitoli2.jpg" alt="" />
				</div><br>
					
				<p>Anda Subrata menjelaskan “pengembang tidak totalitas tidak masuk katagori pengembang artinya dia memanfaatkan fasilitas umum sarana prasarana pemerintah seperti dia membangun dipinggir jalan yang sarana umum jalannya itu jalan pemerintah katagori ini tidak masuk dalam pengembang walaupun membangun perumahan. Dalam konteks pembangunan perumahan berkaitan dengan tersedianya rencana tata ruang apakah sudah memang benar dibangun perumahan, pengembang perumahan harus kompleks, karena akan tersedia fasilitas sarana prasarana utilitas, sekolah, ruang terbuka hijau, perdagangan. Untuk pembuat perumahan sudah terpola pada pola ruang, perumahan boleh tempat usaha karena mendukung aksesbilitas pada masyarakat yang ada dan harus sudah direncanakan dari awal tidak. Ini sebenarnya pengawasan bangunan dan perencanaan izin, disamping mengeluarkan izin harus mengawasi, pada saat perumahan tetapi tidak dibangun sesuai” jelasnya.</p>

				<p>Kabid PSU Dinas PKP Provsu Hadasa Manurung  menambahkan “pada 2017 pemprov masih terlibat pembangunan PSU dalam komplek saat itu sesuai anjuran pemerintah pusat untuk mendukung gerakan sejuta rumah Presiden Jokowi, provinsi membantu pengembang khusus perumahan MBR yang harga jualnya dipatok oleh pemerintah tidak boleh lebih dari 150jt dan penyaluran melalui FLPP itu wajib dibantu oleh pemerintah pusat, provinsi dan daerah kabupaten/kota, bantuan daerah merupakan kemudahan izin pembangunan dan kemudahan lainnya, dari provinsi kita bisa membantu PSU dalam perumahan. Sejak 2020 kami tidak lagi menangani PSU didalam perumahan komplek MBR karena sudah dijelaskan dalam rakortek Bappenas, mana merupakan kewenangan pusat, provinsi dan daerah” tambahnya.</p>

				<p>“Jika DPRD membuat peraturan ini maka harus konsisten bertanggung jawabnya. Kewenangan Provinsi membangun PSU yang ada di daerah perbatasan kabupaten/kota, sementara PSU dalam komplek ke sarana kabupaten/kota. Kewajiban pembangunan PSU dalam Komplek merupakan kewajiban pengembang namun menghubungkan fasilitas drainase atau pembuangan limbah dari dalam kompleks ke fasilitas kab/kota merupakan tanggung jawab Kab/kota. PSU perumahan yang telah ada sebelum berlakunya peraturan daerah ini wajib diserahkan kepada pemeritah daerah, namun bila developer nya sudah tidak dapat ditemui lagi, penghuni komplek bisa menyerahkan asset mereka menjadi jalan desa lalu desa menyerahkan ke kecamatan dan seterusnya sama seperti lingkungan permukiman biasa dan sudah menjadi asset kab/kota”imbuhnya.</p>


