				<p>Medan – Dinas Perumahan dan Kawasan Permukiman Provsu menerima kunjungan kerja DPRD Kab. Serdang Bedagai dan Bappeda Kab. Serdang Bedagai berserta rombongan di Kantor Dinas PKP Provsu Jl. A.H Nasution No. 20 Medan (17/03).</p> 

				<p>Kunjungan kerja disambut oleh Kepala Dinas PKP Provsu Supryanto berserta Sekretaris Dinas PKP Provsu Anda Subrata dan Eselon III dan IV. Kepala Bappeda Sergai Prihatinah menyampaikan ingin meminta arahan dan petunjuk mengenai “Penataan Kawasan rel kereta api oleh PT. KAI di wilayah Desa Deli Muda Hilir, Kec. Perbaungan,  penggusuran ini membuat masyarakat disekitar rel kereta api mengajukan permohonan kepada kecamatan dan kepada DPRD memohon agar mendapat fasilitas untuk pembangunan perumahan yang berada di skala perkotaan seperti pebangunan rusunawa, kami ingin mendapatkan informasi apa bisa program pembangunan rusunawa dilokasi yang sudah MOU dengan PT. KAI” ujar prihatinah.</p>

			<p>Ketua DPRD Kab. Sergai Riski juga menyampaikan hal senada “karena adanya penggusuran oleh PT. KAI, kami berharap adanya program rusunawa ataupun program lain terkait bantuan ke masyarakat karena jika berharap kepada anggaran Kab. Serdang Bedagai  karena banyaknya refocusing untuk penanganan COVID-19 hampir semua OPD tidak bisa bekerja secara maksimal, kami berharap ada arahan dan petunjuk untuk masyarakat di Kab. Serdang Bedagai” jelasnya.</p>

				
