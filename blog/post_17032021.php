			<p>Anda Subrata menjelaskan “memang SPM Dinas PKP Provsu salah satunya merelokasi rumah akibat korban bencana dan merelokasi rumah korban pembangunan pemerintah, namun demikian kami harus mengetahui terlebih dahulu grand desain dari relokasi ini, maupun rencana relokasi ini dari pemerintah daerah kabupaten. Tahun ini kami melakukan relokasi korban banjir di Kab. Madina tahun ini, terkait keinginan bapak/ibu sekalian tidak bisa dilakukan tahun ini, mungkin bisa diusahakan untuk tahun 2022” jelasnya.</p>
				
			<p>Kabid Rumah Umum Saiful Zuhri menambahkan “sepadan kereta api memang banyak dimanfaatkan warga yang tidak mampu, terkait dengan penaganan ini, apakah disiapkan rumah sewanya, rumah susunnya yang kita bantu memang semua masyarakat yang penghasilannya dibawah UMP, Pemerintah Kab. Sergai bisa menyiapkan rencana aksi dn proposal untuk pengusulan rumah susun yang ditanda tangani Bupati ditujukan kepada Kementerian PUPR melalui aplikasi Sibaru, Dinas PKP Provinsi bertugas untuk memonitor apa yang ada di e-planning Sibaru, Bappeda dan DPRD harus bekerjasama dengan Dinas PKP Kab. Sergai dan melampirkan semua berkas proposal” tambahnya.</p>

