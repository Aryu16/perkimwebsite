				<p>Medan – Dinas Perumahan dan Kawasan Permukiman menerima kunjungan kerja dari Komisi V DPRD Kabupaten Karo. Rombongan Komisi V ini dipimpin oleh Inolia Ginting. Pertemuan diadakan di Kantor Dinas PKP Provsu Jalan A.H Nasution Medan (18/6).</p> 

