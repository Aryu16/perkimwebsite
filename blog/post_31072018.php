			<p>Saiful Zuhri, ST, Ir.Pangihutan Marpaung, Ir. Mirna Amin, MT, Ir. Dwityo Akoro Soeranto, MURP, Ir. Ida Mariana, M.Si, Dr. Ir. H. Khalawi AH. M. Sc., MM, Ir. Raden Johny Fajar Sofyan Subrata, MA, Ir. Hardi Simamora, MPL, Ir. M. Hidayat, MM foto bersama dan pemberian ulos setelah pembukaan oleh Dirjen Penyediaan Perumahan dalam Rapat Koordinasi Midterm Review Kegiatan Fasilitasi Pokja PKP Tahun 2018 Regional Barat, Medan (25/07/2018).</p>

			<p>Foto : SNVT Provinsi Sumatera Utara</p>

			<p>Kementerian Pekerjaan Umum dan Perumahan Rakyat melaksanakan Rapat Koordinasi Regional Barat di Kota Medan, 25-27 Juli 2018. Rapat Koordinasi ini diharapkan dapat meningkatkan pemahaman mengenai program perumahan dan kawasan permukiman serta mengevaluasi program kegiatan Tahun Anggaran 2018.</p>

			<p>Acara tersebut dihadiri sejumlah kepala Dinas PKP Provinsi Sumatera Utara dan Kepala Satuan Kerja Penyediaan Perumahan dari lingkungan wilayah barat Indonesia. Dalam acara tersebut Dirjen Penyediaan Perumahan memberikan arahannya bahwa usulan-usulan harus. Dinas PKP dan SNVT harus berkolaborasi menjadi satu untuk mewujudkan percepatan penyediaan perumahan di seluruh penjuru negeri.</p>

			<p>Dalam sambutannya pada pembukaan acara, DR. Ir. H. Khalawi AH. M. Sc, MM selaku Dirjen Penyediaan Perumahan PUPR mengatakan, “SNVT menjadi wakil dirjen di daerah harus menjaga perilaku, etika, eksistensi, dan harus berkomunikasi baik vertikal maupun horizontal untuk mengurangi permasalahan berjenjang dan memutuskan solusi untuk menyelesaikan permasalahan”.</p>

			<p>“Penilaian bukan hanya mengenai pembangunan fisiknya saja, yang terpenting bagaimana cara mengurangi backlog. Dirjen dibantu Satgas satu juta rumah yang berfungsi mengevaluasi dan mengawasi kinerja satker daerah untuk mewujudkan program percepatan pembangunan perumahan dan kawasan permukiman.” Tambahnya.</p>

			<p>Dalam acara ini  juga membahas mengenai Kebijakan Layanan Dukungan Managemen dan Operasional, Kebijakan dan Strategi Penyelenggaraan Perumahan dan Evaluasi Kegiatan tahun 2018. Sinergitas Pokja PPAS Nasional dan Pokja PKP Daerah dalam Pembangunan PKP, New Urban Agenda, Rancangan RPJMN 2020-2024, dan menyusun RKKL yang efektif. Meningkatkan kesadaran setiap daerah akan perlunya Pokja PKP dengan langkah memberikan apresiasi kepada daerah-daerah yang sudah membentuk Pokja PKP dan mengaplikasikannya dengan menyiapkan dokumen RP3KP.</p>

			<p>Selain itu untuk mempercepat Program satu juta rumah harus diadakan sosialisasi di daerah bersama Dinas PKP Provinsi Sumatera Utara dan pokja untuk memberikan informasi kepada masyarakat yang belum memahami program satu juta rumah.</p>

			<p>Kata sambutan diakhiri dengan pemberian ulos dari Kepala Dinas PKP Provinsi Sumatera Utara, Ir. Ida Mariana M. Si dan Kepala Satuan Kerja Penyediaan Perumahan Provinsi Sumatera Utara kepada Dirjen Penyediaan Perumahan, Direktur Perencanaan, Direktur Rumah Swadaya, Direktur Rumah Susun  dan beberapa Pakar Perumahan.</p>


