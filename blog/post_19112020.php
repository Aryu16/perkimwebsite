			<p>Beberapa masukan juga disampaikan oleh Camat Belawan, Kelompok Masyarakat Belawan, Pemkab Deli Serdang, Syahbandar Belawan mengenai usulan yang akan didiskusikan nantinya bersama-sama di Kantor Pelindo I agar menemukan jalan terbaik untuk pembangunan Utara Medan.</p>

