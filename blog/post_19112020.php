			<p>Pjs Walikota Medan Arief S. Trinugroha menyampaikan perlu disusun strategi awal yang matang terlebih dahulu terkait dengan legalitas lahan. Lahan yang tersedia merupakan lahan HPL yang dikuasai oleh Pelindo I, sehingga harus diketahui penjelasan pengelolaan pada lahan tersebut, jelasnya.</p>

			<div class="image">
